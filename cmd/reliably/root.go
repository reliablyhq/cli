func shouldCheckForUpdate() bool {
	if os.Getenv("RELIABLY_NO_UPDATE_NOTIFIER") != "" {
		return false
	}

	return updaterRepo != "" && version != "DEV" &&
		!ctx.IsCI() && !isCompletionCommand() && !isUpdateCommand() &&
		utils.IsTerminal(os.Stderr)
}

func checkForUpdate(currentVersion string) (*update.ReleaseInfo, error) {
	if !shouldCheckForUpdate() {
		return nil, nil
	}

	repo := updaterRepo
	stateFilePath := path.Join(config.ConfigDir, "release.yml")
	return update.CheckForUpdate(nil, stateFilePath, repo, currentVersion)
}

func isCompletionCommand() bool {
	return len(os.Args) > 1 && os.Args[1] == "completion"
}

func isUpdateCommand() bool {
	return len(os.Args) > 1 && os.Args[1] == "update"
}

// CheckOverriddenOrg sets into the loaded config the org info (ID & name)
// from the overridden RELIABLY_ORG env var that accepts either an org UIUD
// or an org slug/name
func CheckOverriddenOrg() {

	if org := os.Getenv(config.RELIABLY_ORG); org != "" {

		var o *api.Organization
		var err error

		h := config.Hostname
		apiClient := api.NewClientFromHTTP(api.AuthHTTPClient(h))

		switch {
		case utils.IsValidUUID(org):
			o, err = api.GetOrganisation(apiClient, h, org)
		default:
			o, err = api.GetOrganizationByName(apiClient, h, org)
		}

		if err == nil {
			config.OverriddenOrg = &config.OrgInfo{
				ID:   o.ID,
				Name: o.Name,
			}
		}
		if err != nil {
			log.Debug(err)
		}

	}
}
