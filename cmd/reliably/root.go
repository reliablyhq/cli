func NewCmdRoot() *cobra.Command {
	cmd := &cobra.Command{
		Use:               "reliably <command> [flags]",
		Short:             "Reliably CLI",
		Long:              `The Reliably Command Line Interface (CLI).`,
		Version:           version,
		SilenceErrors:     true, // quiet errors down stream
		SilenceUsage:      true, //silence usage when an error occurs
		DisableAutoGenTag: true, // disables auto gen tag "Auto generated by spf13/cobra..."
		//Example: heredoc.Doc(``),
		Annotations: map[string]string{
			"help:feedback": heredoc.Doc(`

Feedback:
  You can provide with feedback or report an issue at https://github.com/reliablyhq/cli/issues/new
`),
			"help:environment": heredoc.Doc(`

Environment variables:
  See 'reliably environment --help' for the list of supported environment variables.
`),
		},
	}

	cmd.PersistentFlags().BoolVarP(
		&verbose, "verbose", "v", false, "verbose output")
	cmd.PersistentFlags().BoolVar(
		&noColor, "no-color", false, "Disable color output")
	cmd.SetVersionTemplate(FormatVersion(version, buildDate, revision))
	cmd.PersistentPreRunE = func(cmd *cobra.Command, args []string) error {
		// This prerun hook is inherited by subcommands if not overridden
		// If this hook is needed by a subcommand, makes sure to excplicitely
		// call that root hook as:
		// cmd.Root().PersistentPreRunE(cmd, args)

		if err := setUpVerboseLogLevel(verbose); err != nil {
			return err
		}

		// any command should be run with authentication unless explicitly disabled
		doCheck := cmd != nil && cmd.Name() != "help" && cmdutil.IsAuthCheckEnabled(cmd)
		if doCheck && !cmdutil.CheckAuth() {
			cmdutil.PrintRequireAuthMsg()
			// force exit not to have colored error message,
			// as we handle more user-friendly message
			os.Exit(1)
		}

		return nil
	}
	cmd.Flags().Bool("help", false, "help for reliably")
	cmd.Flags().Bool("version", false, "version for reliably") // requires explicit or not shown with `reliably help`

	// Add commands
	cmd.AddCommand(NewCmdAuth())
	cmd.AddCommand(NewCmdDiscover()) // deprecated
	cmd.AddCommand(NewCmdCompletion())
	cmd.AddCommand(NewCmdVersion())
	cmd.AddCommand(NewCmdWorkflow())
	cmd.AddCommand(NewCmdHistory())
	cmd.AddCommand(NewCmdScan(cmd))
	cmd.AddCommand(NewCmdUpdate())
	cmd.AddCommand(org.NewCommand())
	cmd.AddCommand(slo.NewCommand())

	if v.IsDevVersion() {
		cmd.AddCommand(alpha.NewCommand())
		cmd.AddCommand(beta.NewCommand())
	}

	//Help topics
	cmd.AddCommand(NewHelpTopic("environment"))

	// disable auth chekc for root command itself
	cmdutil.DisableAuthCheck(cmd)

	noColor := strings.Contains(strings.Join(os.Args, " "), "--no-color")
	// force disabling coloring before we render the template or
	// help will always be colored even when flag is set - but not parsed yet -
	// because when using --help , persistent pre run is not yet executed
	if noColor || os.Getenv("NO_COLOR") != "" {
		disableColor()
	} else {
		customizeColor()
	}

	// Uses a custom usage template - for adding feedback section to help -
	template := cmdutil.CustomUsageTemplate(cmd)
	cmd.SetUsageTemplate(template)

	return cmd
}

// Execute the root command and exit with nonzero code in case of errors
func Execute() {
	updateMessageChan := make(chan *update.ReleaseInfo)
	go func() {
		rel, _ := checkForUpdate(version)
		updateMessageChan <- rel
	}()

	verbose = config.IsDebugMode()
	CheckOverriddenOrg()

	rootCmd := NewCmdRoot()
	if err := rootCmd.Execute(); err != nil {
		er(err)
	}

	newRelease := <-updateMessageChan
	if newRelease != nil {
		color.Cyan()
		upgradeCommand := getUpgradeCommand()
		msg := fmt.Sprintf("%s %s → %s\n\n%s\n%s%s",
			color.Yellow("A new release of reliably is available:"),
			color.Cyan(strings.TrimPrefix(version, "v")),
			color.Cyan(strings.TrimPrefix(newRelease.Version, "v")),
			color.Yellow("Details of the new release can be found here:"),
			newRelease.URL, upgradeCommand)

		fmt.Fprintf(os.Stderr, "\n\n%s\n\n", msg)
	}
}

